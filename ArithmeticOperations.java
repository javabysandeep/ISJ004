public class ArithmeticOperations {

	public static void main(String[] args){

		int a = 100;
		int b = 200;	
		int c = a + b;

		System.out.println(c);	//300

		System.out.println(100 + 200); //300	

		System.out.println(200 - 50);// 150

		System.out.println(200 * 5);//1000

		System.out.println(200/5);//40

		System.out.println(200 % 5);//0	

	}
}