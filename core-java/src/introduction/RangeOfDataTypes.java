package introduction;

public class RangeOfDataTypes {

	public static void main(String[] args){
	
		System.out.println("byte "+Byte.MIN_VALUE+"\t"+Byte.MAX_VALUE);

		System.out.println("short "+Short.MIN_VALUE+"\t"+Short.MAX_VALUE);

		System.out.println("int "+Integer.MIN_VALUE+"\t"+Integer.MAX_VALUE);

		System.out.println("long "+Long.MIN_VALUE+"\t"+Long.MAX_VALUE);


		System.out.println("float "+Float.MIN_VALUE+"\t"+Float.MAX_VALUE);


		System.out.println("double "+Double.MIN_VALUE+"\t"+Double.MAX_VALUE);

		System.out.println("char "+(int)Character.MIN_VALUE+"\t"+(int)Character.MAX_VALUE);

		

	}
}