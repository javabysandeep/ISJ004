package controlStatements.conditional.ifElseDemos;

public class MaximumOfTwoNumbers {
    public static void main(String[] args) {
        int number1 = 100;
        int number2 = 200;
        if (number1 > number2) {
            System.out.println(number1);
        } else {
            System.out.println(number2);
        }

    }

}
