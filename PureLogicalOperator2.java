public class PureLogicalOperator2 {

	public static void main(String[] args){
		
		System.out.println( (10 > 5) && (10 < 11));//true 
		System.out.println( (10 > 5) || (10 < 11));//true 
		System.out.println( (10 > 50) || (10 < 11));//true 
		System.out.println( (10 > 5) || (100 < 11));//true 
	
		

	}	
}